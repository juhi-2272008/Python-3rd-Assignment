#program to find square root, logarithm and sine value of a number 

import math

num = float(input("Enter a number: "))
square_num = math.sqrt(num)  # Find a square root of number
log_num = math.log(num)    # Find a logarithm of number
sine_num = math.sin(num)   #Sine of values

print(f"Squqre root: {square_num}")
print(f"logarithm: {log_num}")
print(f"Sine: {sine_num}")